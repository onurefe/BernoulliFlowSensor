/* USER CODE BEGIN 4 */
void Flow_MeasurementCompletedCb(float xVelocity, float yVelocity)
{
  int32_t x_int, y_int;
  int idx = 0;

  x_int = (int32_t)(xVelocity * 1000.0);
  if (x_int < 0.0f)
    x_int = -x_int;

  USBTxBuffer[idx++] = 'X';
  USBTxBuffer[idx++] = ':';

  if (xVelocity < 0.0f)
    USBTxBuffer[idx++] = '-';

  idx = toCharArray(&USBTxBuffer[idx], (x_int / 1000));
  USBTxBuffer[idx++] = '.';
  idx = toCharArray(&USBTxBuffer[idx], (x_int % 1000));

  if (Flow_IsEnabledYAxis())
  {
    y_int = (int32_t)(yVelocity * 1000.0);

    if (y_int < 0.0f)
      y_int = -y_int;

    USBTxBuffer[idx++] = ' ';
    USBTxBuffer[idx++] = 'Y';
    USBTxBuffer[idx++] = ':';

    if (yVelocity < 0.0f)
      USBTxBuffer[idx++] = '-';
    
    idx = toCharArray(&USBTxBuffer[idx], (y_int / 1000));
    USBTxBuffer[idx++] = '.';
    idx = toCharArray(&USBTxBuffer[idx], (y_int % 1000));
  }

  USBTxBuffer[idx++] = '\n';
  USBTxBuffer[idx++] = '\r';

  CDC_Transmit_FS(USBTxBuffer, idx);
}

static uint16_t toCharArray(uint8_t *buffer, int32_t num)
{
  int i, rem, len = 0, n;

  n = num;
  while (n != 0)
  {
    len++;
    n /= 10;
  }
  for (i = 0; i < len; i++)
  {
    rem = num % 10;
    num = num / 10;
    buffer[len - (i + 1)] = rem + '0';
  }

  return len;
}

